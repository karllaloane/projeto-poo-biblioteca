package biblioteca;

/** Interface que prevê a implementação do método retornaDados().
 * 
 * @release 1.0
 * @since release 1
 */
public interface Publicavel {
    /** Método que deve ser implementado pelas classes que adotarem esta interface,
     * que deve retornar uma String com os dados dos atributos da classe
     *  
     * @return String
     */
	public String retornaDados();
}
