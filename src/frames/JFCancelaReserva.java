    private void jBPesquisarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarLivroActionPerformed

        item = null;
        jTFLivro.setText("");
        jTFNome.setText("");
        jBConfirma.setEnabled(false);

        ArrayList<Livro> livros = new ArrayList<>();
        ArrayList<Periodico> periodico = new ArrayList<>();

        String texto = jTFLivroPesquisa.getText();

        if(texto.equals("")){
            JOptionPane.showMessageDialog(null, "Digite o título!", "Error", JOptionPane.ERROR_MESSAGE);
            jTFLivro.setText("");
        } else if(!jRBPeriodico.isSelected() && !jRBTitulo.isSelected()){
            JOptionPane.showMessageDialog(null, "Selecione o tipo de busca (Livro ou Periódico)!", "Error", JOptionPane.ERROR_MESSAGE);
            jTFLivro.setText("");
        } else{

            if(jRBPeriodico.isSelected()){
                periodico = biblioteca.listarPeriodicosNome(texto);

                if(periodico.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Nenhum registro encontrado!", "Error", JOptionPane.ERROR_MESSAGE);
                    jTFLivro.setText("");
                } else {
                    limparTabela();

                    String[] linha = new String[] {null, null, null};
                    for (int i = 0; i < periodico.size(); i++) {
                        tmLivro.addRow(linha);
                        tmLivro.setValueAt(periodico.get(i).getTitulo(), i, 0);
                        tmLivro.setValueAt(periodico.get(i).getEditora(), i, 1);
                        if(periodico.get(i).isDisponivel()){
                            tmLivro.setValueAt("Disponível", i, 2);
                        } else {
                            tmLivro.setValueAt("Indisponível", i, 2);
                        }
                    }
                }

            } else {
                livros = biblioteca.listarLivrosNome(texto);

                if(livros.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Nenhum registro encontrado!", "Error", JOptionPane.ERROR_MESSAGE);
                    jTFLivro.setText("");
                } else {
                    limparTabela();

                    String[] linha = new String[] {null, null, null};
                    // P/ cada registro é criada uma nova linha, cada linha recebe os campos do registro
                    for (int i = 0; i < livros.size(); i++) {

                        tmLivro.addRow(linha);
                        tmLivro.setValueAt(livros.get(i).getTitulo(), i, 0);
                        tmLivro.setValueAt(livros.get(i).getAutor(), i, 1);
                        if(livros.get(i).isDisponivel()){
                            tmLivro.setValueAt("Disponível", i, 2);
                        } else {
                            tmLivro.setValueAt("Indisponível", i, 2);
                        }
                    }
                }
            }
        }

        jTFLivroPesquisa.setText("");
    }//GEN-LAST:event_jBPesquisarLivroActionPerformed

