    private void jBConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConfirmaActionPerformed
        // TODO add your handling code here:
        if(item != null){
            item.setReservado(false);
            item.setReserva(null);
            
            JOptionPane.showMessageDialog(null, "Reserva do ítem/Periódico cancelada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            
            item = null;
            jTFLivro.setText("");
            jTFNome.setText("");
            jBConfirma.setEnabled(false);
            limparTabela();

        } else {
            JOptionPane.showMessageDialog(null, "Falha ao cancelar reserva!", "Falha", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBConfirmaActionPerformed

    private void limparTabela() {       
        while (tmLivro.getRowCount() > 0) {            
            tmLivro.removeRow(0);
        }
    }
