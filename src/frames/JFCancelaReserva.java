    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = jTable1.getSelectedRow();

        try{
            //procurando pelo objeto com nome exato
            item = biblioteca.procurarItem((String)jTable1.getValueAt(linhaSelecionada, 0));

            if(!item.isReservado()){
                JOptionPane.showMessageDialog(null, "O Livro/Periódico não possui reservas!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                jTFLivro.setText(item.getTitulo());
                jTFNome.setText(item.getReserva().getCliente().getNome());
                jBConfirma.setEnabled(true);
            }
        } catch (BuscaFalhouException e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Aviso", JOptionPane.INFORMATION_MESSAGE);
            jTFLivro.setText("");
            jBConfirma.setEnabled(false);
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

